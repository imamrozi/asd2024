public class LoopBasic {
    public static void main(String[] args) {
        /* 
        1. inisialiasi (starting point)
        2. Kondisi
        3. iterasi (step)
        */ 
        int i = 1;//posisi awal
        while(i>10){//kondisi
            System.out.println(i);
            i = i+1;
        }
        
    }
}
